def luas_persegi (sisi):
    luas = sisi*sisi
    print("luas persegi = " + str (luas) )

luas_persegi (20)

def luas_persegipanjang (panjang, lebar):
    luas = panjang*lebar
    print("luas persegi panjang = " + str (luas) )

luas_persegipanjang (12, 5)

